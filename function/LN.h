double ln(double exponent){
    double ans;
    ans = 0;
    for(unsigned short int i; i < 32; i++){
        ans += ez_pow((exponent - 1) / (exponent + 1), 2 * i + 1) * odd_reciprocal_list_for_ln[i];
    }
    return ans;
}

